"""Repair truncated consolidated test modules by reconstructing from backups.

Scans `tests/` for files that contain only the autogenerated header and replaces
them with combined contents found under `backups/converted_preview/*/tests/`.

This script is safe: it backs up any file it will overwrite under
`backups/consolidated_originals/<timestamp>/`.
"""

from __future__ import annotations

import re
import shutil
from collections import OrderedDict
from datetime import datetime
from pathlib import Path
from typing import List

ROOT = Path(__file__).resolve().parents[1]
BACKUP_DIR = ROOT / "backups" / "consolidated_originals" / datetime.utcnow().strftime("%Y%m%dT%H%M%SZ")
CONVERTED_PREVIEW = ROOT / "backups" / "converted_preview"


HEADER_RE = re.compile(r"# Auto-generated consolidated test module", re.IGNORECASE)
IMPORT_RE = re.compile(r"^(from\s+\S+\s+import\b.*|import\s+.+)$")
DOCSTRING_RE = re.compile(r"^\s*(?:['\"]{3})(.*?)(?:['\"]{3})", re.S)


def find_fragments_by_prefix() -> dict[str, List[Path]]:
    """Return mapping from prefix stem (e.g., test_pipeline_stages) to ordered fragment paths."""
    mapping: dict[str, List[Path]] = {}
    if not CONVERTED_PREVIEW.exists():
        return mapping
    for preview in CONVERTED_PREVIEW.iterdir():
        tests_dir = preview / "tests"
        if not tests_dir.exists():
            continue
        for p in tests_dir.rglob("*.py"):
            name = p.name
            m = re.match(r"^(test_[a-zA-Z0-9_]+?)_(\d{4})\.py$", name)
            if not m:
                continue
            stem = m.group(1)
            mapping.setdefault(stem, []).append(p)
    # sort fragments by numeric suffix
    for k, lst in mapping.items():
        mapping[k] = sorted(lst, key=lambda p: int(re.search(r"_(\d{4})\.py$", p.name).group(1)))
    return mapping


def find_fragments_by_name() -> dict[str, Path]:
    """Return mapping from exact basename to a fragment path (first match)."""
    mapping: dict[str, Path] = {}
    if not CONVERTED_PREVIEW.exists():
        return mapping
    for preview in CONVERTED_PREVIEW.iterdir():
        tests_dir = preview / "tests"
        if not tests_dir.exists():
            continue
        for p in tests_dir.rglob("*.py"):
            if p.name not in mapping:
                mapping[p.name] = p
    return mapping


def extract_parts(text: str) -> tuple[str | None, List[str], str]:
    """Return (docstring, imports, body) for a fragment text.

    docstring: first module docstring if present
    imports: list of import lines (no trailing newlines)
    body: remaining text
    """
    s = text.lstrip()
    doc = None
    if s.startswith(('"""', "'''")):
        m = DOCSTRING_RE.match(s)
        if m:
            doc = m.group(0).strip()
            s = s[len(m.group(0)) :].lstrip()
    else:
        # heuristic: if the file starts with plain English-like lines (no imports/defs),
        # treat the initial paragraph as a module docstring and wrap it.
        first_lines = []
        for line in s.splitlines():
            stripped = line.strip()
            if not stripped:
                # blank line ends the header paragraph
                break
            # stop if line looks like code
            if re.match(r'^(import\b|from\b|def\b|class\b|#|@|"""|\'\'\')', stripped):
                first_lines = []
                break
            first_lines.append(line)
        if first_lines:
            doc_text = "\n".join(line.rstrip() for line in first_lines)
            # wrap into triple-quoted module docstring
            doc = '"""' + "\n" + doc_text + "\n" + '"""'
            # remove those lines from s
            s = s[len("\n".join(first_lines)) :].lstrip()
    imports: List[str] = []
    body_lines: List[str] = []
    in_imports = True
    for line in s.splitlines():
        if in_imports and (IMPORT_RE.match(line.strip()) or line.strip() == ""):
            if IMPORT_RE.match(line.strip()):
                imports.append(line.rstrip())
            else:
                # blank line while in imports -> keep as separation
                pass
        else:
            in_imports = False
            body_lines.append(line)
    body = "\n".join(body_lines).strip()
    return doc, imports, body


def dedupe_imports(import_lists: List[List[str]]) -> List[str]:
    # flatten while preserving order and uniqueness, but keep __future__ first
    seen = OrderedDict()
    future_lines: List[str] = []
    others: List[str] = []
    for lst in import_lists:
        for imp in lst:
            imp_key = imp.strip()
            if imp_key in seen:
                continue
            seen[imp_key] = True
            if imp_key.startswith("from __future__"):
                future_lines.append(imp_key)
            else:
                others.append(imp_key)
    return future_lines + others


def repair_all() -> int:
    fragments_map = find_fragments_by_prefix()
    name_map = find_fragments_by_name()
    repaired = 0
    BACKUP_DIR.mkdir(parents=True, exist_ok=True)
    for target in (ROOT / "tests").rglob("test_*.py"):
        try:
            text = target.read_text(encoding="utf8")
        except Exception:
            continue
        if not HEADER_RE.search(text):
            continue
        stem = target.stem
        frags = fragments_map.get(stem)
        # fallback: exact basename match in name_map
        if not frags:
            exact = name_map.get(target.name)
            if exact:
                frags = [exact]
        if not frags:
            print(f"no fragments found for {target.name}")
            continue
        # collect docstring from first fragment, imports from all, and bodies
        docstring = None
        all_imports: List[List[str]] = []
        bodies: List[str] = []
        for frag in frags:
            frag_text = frag.read_text(encoding="utf8").strip()
            frag_text = re.sub(r"^```python\n|\n```$", "", frag_text, flags=re.MULTILINE)
            doc, imports, body = extract_parts(frag_text)
            if docstring is None and doc:
                docstring = doc
            all_imports.append(imports)
            if body:
                bodies.append(body)

        imports = dedupe_imports(all_imports)
        parts: List[str] = []
        if docstring:
            parts.append(docstring)
        if imports:
            parts.append("\n".join(imports))
        parts.append("\n\n".join(bodies))
        new_content = "\n\n".join([p for p in parts if p]).rstrip() + "\n"

        # backup and write
        shutil.copy2(target, BACKUP_DIR / target.name)
        target.write_text(new_content, encoding="utf8")
        print(f"reconstructed {target}")
        repaired += 1
    print(f"done: repaired={repaired}")
    return 0


if __name__ == "__main__":
    raise SystemExit(repair_all())
